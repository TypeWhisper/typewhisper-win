/// </summary>
public enum PluginLogLevel
{
    /// <summary>
    /// Represents the debug option.
    /// </summary>
    Debug,
    /// <summary>
    /// Represents the info option.
    /// </summary>
    Info,
    /// <summary>
    /// Represents the warning option.
    /// </summary>
    Warning,
    /// <summary>
    /// Represents the error option.
    /// </summary>
    Error
}
